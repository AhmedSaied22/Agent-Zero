  static String getRandomItem(CardType cardType) {
    final random = Random();
    switch (cardType) {
      case CardType.jobs:
        return _jobs[random.nextInt(_jobs.length)];
      case CardType.countries:
        return _countries[random.nextInt(_countries.length)];
      case CardType.famousPlaces:
        return _famousPlaces[random.nextInt(_famousPlaces.length)];
      case CardType.publicPlaces:
        return _publicPlaces[random.nextInt(_publicPlaces.length)];
      case CardType.foods:
        return _foods[random.nextInt(_foods.length)];
      case CardType.footballPlayers:
        return _footballPlayers[random.nextInt(_footballPlayers.length)];
    }
